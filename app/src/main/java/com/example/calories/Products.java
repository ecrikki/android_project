package com.example.calories;

public class Products {
    public String белки, жиры, углеводы, калорийность;

    public Products() {
    }

    public Products(String белки, String жиры, String углеводы, String калорийность) {
        this.белки = белки;
        this.жиры = жиры;
        this.углеводы = углеводы;
        this.калорийность = калорийность;
    }
}